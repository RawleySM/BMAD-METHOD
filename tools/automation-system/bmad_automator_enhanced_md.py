class MarkdownConfigParser:
    """Parse project configuration from Markdown files generated by PrompEnhancer agent"""
    
    def __init__(self, md_file_path: str):
        self.md_file_path = Path(md_file_path)
        self.config = {}
    
    def parse(self) -> Dict[str, Any]:
        """Parse the Markdown file and extract project configuration"""
        if not self.md_file_path.exists():
            bmad_logger.logger.error(f"Markdown config file not found: {self.md_file_path}")
            raise FileNotFoundError(f"Config file not found: {self.md_file_path}")
        
        try:
            content = self.md_file_path.read_text(encoding='utf-8')
            bmad_logger.logger.info(f"Parsing Markdown config from {self.md_file_path}")
            
            # Parse different sections
            self.config = {
                "project_details": self._parse_project_details(content),
                "constraints": self._parse_constraints(content),
                "technical_requirements": self._parse_technical_requirements(content),
                "bmad_settings": self._parse_bmad_settings(content),
                "metadata": {
                    "source_file": str(self.md_file_path),
                    "parsed_at": datetime.now().isoformat()
                }
            }
            
            bmad_logger.logger.info(f"Successfully parsed Markdown config: {self.config['project_details'].get('name', 'Unnamed Project')}")
            return self.config
            
        except Exception as e:
            bmad_logger.logger.error(f"Error parsing Markdown config: {e}")
            raise
    
    def _parse_project_details(self, content: str) -> Dict[str, Any]:
        """Extract project details from Markdown content"""
        details = {}
        
        # Extract project name from title
        title_match = re.search(r'^#\s+(.+)$', content, re.MULTILINE)
        if title_match:
            details["name"] = title_match.group(1).strip()
        
        # Extract description from Project Overview section
        overview_match = re.search(r'##\s+Project\s+Overview\s*\n(.*?)(?=\n##|\n---|\Z)', content, re.DOTALL | re.IGNORECASE)
        if overview_match:
            details["description"] = overview_match.group(1).strip()
        
        # Extract goals
        goals_match = re.search(r'##\s+Goals?\s*\n(.*?)(?=\n##|\n---|\Z)', content, re.DOTALL | re.IGNORECASE)
        if goals_match:
            goals_text = goals_match.group(1).strip()
            # Extract bullet points
            goals = re.findall(r'[-*]\s+(.+)', goals_text)
            details["goals"] = goals
        
        return details
    
    def _parse_constraints(self, content: str) -> Dict[str, Any]:
        """Extract project constraints from Markdown content"""
        constraints = {}
        
        # Extract budget information
        budget_match = re.search(r'##\s+Budget\s*\n(.*?)(?=\n##|\n---|\Z)', content, re.DOTALL | re.IGNORECASE)
        if budget_match:
            budget_text = budget_match.group(1)
            
            # Extract total budget
            total_match = re.search(r'\$?([\d,]+)', budget_text)
            if total_match:
                total_str = total_match.group(1).replace(',', '')
                constraints["budget"] = {"total_usd": int(total_str)}
            
            # Extract timeline
            timeline_match = re.search(r'(\d+)\s*weeks?', budget_text, re.IGNORECASE)
            if timeline_match:
                if "budget" not in constraints:
                    constraints["budget"] = {}
                constraints["budget"]["timeline_weeks"] = int(timeline_match.group(1))
        
        # Extract technical constraints
        tech_match = re.search(r'##\s+Technical\s+Constraints\s*\n(.*?)(?=\n##|\n---|\Z)', content, re.DOTALL | re.IGNORECASE)
        if tech_match:
            tech_text = tech_match.group(1)
            
            # Extract compute resources
            compute_resources = []
            compute_matches = re.findall(r'[-*]\s+(.+(?:CPU|RAM|GPU|Memory|Storage).+)', tech_text, re.IGNORECASE)
            constraints["compute_resources"] = compute_matches
        
        # Extract tech stack
        stack_match = re.search(r'##\s+Tech\s+Stack\s*\n(.*?)(?=\n##|\n---|\Z)', content, re.DOTALL | re.IGNORECASE)
        if stack_match:
            stack_text = stack_match.group(1)
            tech_stack = re.findall(r'[-*]\s+(.+)', stack_text)
            constraints["tech_stack"] = tech_stack
        
        return constraints
    
    def _parse_technical_requirements(self, content: str) -> Dict[str, Any]:
        """Extract technical requirements from Markdown content"""
        requirements = {}
        
        # Extract architecture requirements
        arch_match = re.search(r'##\s+Architecture\s*\n(.*?)(?=\n##|\n---|\Z)', content, re.DOTALL | re.IGNORECASE)
        if arch_match:
            arch_text = arch_match.group(1)
            requirements["architecture"] = re.findall(r'[-*]\s+(.+)', arch_text)
        
        # Extract deployment requirements
        deploy_match = re.search(r'##\s+Deployment\s*\n(.*?)(?=\n##|\n---|\Z)', content, re.DOTALL | re.IGNORECASE)
        if deploy_match:
            deploy_text = deploy_match.group(1)
            requirements["deployment"] = re.findall(r'[-*]\s+(.+)', deploy_text)
        
        return requirements
    
    def _parse_bmad_settings(self, content: str) -> Dict[str, Any]:
        """Extract BMAD-specific settings from Markdown content"""
        bmad_settings = {}
        
        # Extract BMAD configuration section
        bmad_match = re.search(r'##\s+BMAD\s+Configuration\s*\n(.*?)(?=\n##|\n---|\Z)', content, re.DOTALL | re.IGNORECASE)
        if bmad_match:
            bmad_text = bmad_match.group(1)
            
            # Extract IDE preference
            ide_match = re.search(r'IDE:\s*(.+)', bmad_text, re.IGNORECASE)
            if ide_match:
                bmad_settings["preferred_ide"] = ide_match.group(1).strip()
            
            # Extract expansion packs
            expansion_match = re.search(r'Expansion\s+Packs?:\s*\n(.*?)(?=\n\w|\Z)', bmad_text, re.DOTALL | re.IGNORECASE)
            if expansion_match:
                expansion_text = expansion_match.group(1)
                expansions = re.findall(r'[-*]\s+(.+)', expansion_text)
                bmad_settings["expansion_packs"] = expansions
        
        # Default settings if not specified
        if "preferred_ide" not in bmad_settings:
            bmad_settings["preferred_ide"] = "claude-code"
        
        if "expansion_packs" not in bmad_settings:
            bmad_settings["expansion_packs"] = []
        
        return bmad_settings

# --- BMAD Installation Manager ---

class BMADInstaller:
    """Handles BMAD-METHOD installation and initialization"""
    
    def __init__(self, project_dir: Path, config: Dict[str, Any]):
        self.project_dir = project_dir
        self.config = config
        self.bmad_dir = project_dir / ".bmad-core"
    
    def is_bmad_installed(self) -> bool:
        """Check if BMAD is already installed in the project directory"""
        return self.bmad_dir.exists() and (self.bmad_dir / "core-config.yaml").exists()
    
    async def install_bmad_method(self) -> bool:
        """Install BMAD-METHOD using npx"""
        try:
            bmad_logger.logger.info(f"Installing BMAD-METHOD in {self.project_dir}")
            
            # Check if Node.js is available
            try:
                result = subprocess.run(["node", "--version"], capture_output=True, text=True, timeout=10)
                if result.returncode != 0:
                    bmad_logger.logger.error("Node.js not found. BMAD-METHOD requires Node.js")
                    return False
                bmad_logger.logger.info(f"Node.js version: {result.stdout.strip()}")
            except (subprocess.TimeoutExpired, FileNotFoundError):
                bmad_logger.logger.error("Node.js not available. Cannot install BMAD-METHOD")
                return False
            
            # Install BMAD-METHOD using npx
            install_cmd = ["npx", "bmad-method@latest", "install", "--full", "--directory", str(self.project_dir)]
            
            # Add IDE configuration
            bmad_settings = self.config.get("bmad_settings", {})
            preferred_ide = bmad_settings.get("preferred_ide", "claude-code")
            install_cmd.extend(["--ide", preferred_ide])
            
            # Add expansion packs if specified
            expansion_packs = bmad_settings.get("expansion_packs", [])
            if expansion_packs:
                install_cmd.extend(["--expansion-packs"] + expansion_packs)
            
            bmad_logger.logger.info(f"Running BMAD install command: {' '.join(install_cmd)}")
            
            # Run the installation
            process = subprocess.Popen(
                install_cmd,
                cwd=self.project_dir,
                stdout=subprocess.PIPE,
                stderr=subprocess.PIPE,
                text=True
            )
            
            # Monitor the process
            stdout, stderr = process.communicate(timeout=300)  # 5 minute timeout
            
            if process.returncode == 0:
                bmad_logger.logger.info("BMAD-METHOD installation completed successfully")
                bmad_logger.log_bmad_installation(str(self.project_dir), True, "Installation successful")
                return True
            else:
                error_msg = f"BMAD installation failed with return code {process.returncode}"
                if stderr:
                    error_msg += f"\nError output: {stderr}"
                bmad_logger.logger.error(error_msg)
                bmad_logger.log_bmad_installation(str(self.project_dir), False, error_msg)
                return False
                
        except subprocess.TimeoutExpired:
            bmad_logger.logger.error("BMAD installation timed out after 5 minutes")
            bmad_logger.log_bmad_installation(str(self.project_dir), False, "Installation timeout")
            return False
        except Exception as e:
            error_msg = f"Error during BMAD installation: {e}"
            bmad_logger.logger.error(error_msg)
            bmad_logger.log_bmad_installation(str(self.project_dir), False, error_msg)
            return False
    
    def configure_bmad_for_claude_code(self) -> bool:
        """Configure BMAD to work with Claude Code CLI"""
        try:
            core_config_path = self.bmad_dir / "core-config.yaml"
            if not core_config_path.exists():
                bmad_logger.logger.error("BMAD core-config.yaml not found")
                return False
            
            # Read the current configuration
            import yaml
            with open(core_config_path, 'r') as f:
                core_config = yaml.safe_load(f)
            
            # Update configuration for Claude Code integration
            if 'agents' not in core_config:
                core_config['agents'] = {}
            
            # Add Claude Code as the primary agent administrator
            core_config['agents']['primary_administrator'] = 'claude-code'
            core_config['agents']['claude_code_integration'] = True
            
            # Add project-specific settings from our config
            project_details = self.config.get("project_details", {})
            if project_details.get("name"):
                core_config['project_name'] = project_details["name"]
            if project_details.get("description"):
                core_config['project_description'] = project_details["description"]
            
            # Write back the updated configuration
            with open(core_config_path, 'w') as f:
                yaml.dump(core_config, f, default_flow_style=False, sort_keys=False)
            
            bmad_logger.logger.info("Successfully configured BMAD for Claude Code integration")
            return True
            
        except Exception as e:
            bmad_logger.logger.error(f"Error configuring BMAD for Claude Code: {e}")
            return False

