# Set up structured logging
class BMadLogger:
    def __init__(self, log_file: str = "bmad_decisions.log"):
        self.log_file = Path(log_file)
        self.setup_logging()
    
    def setup_logging(self):
        """Setup structured logging with file and console output"""
        logging.basicConfig(
            level=logging.INFO,
            format="%(asctime)s - %(name)s - %(levelname)s - %(message)s",
            handlers=[
                logging.FileHandler(self.log_file),
                logging.StreamHandler(sys.stderr)
            ]
        )
        self.logger = logging.getLogger("bmad_automator")
    
    def log_decision(self, hook_data: Dict[str, Any], decision: str, reasoning: str, execution_time: float):
        """Log decision with full context for audit trail"""
        decision_record = {
            "timestamp": datetime.now().isoformat(),
            "session_id": hook_data.get("session_id"),
            "hook_event": hook_data.get("hook_event_name"),
            "tool_name": hook_data.get("tool_name"),
            "decision": decision,
            "reasoning": reasoning,
            "execution_time_seconds": execution_time,
            "cwd": hook_data.get("cwd")
        }
        
        self.logger.info(f"DECISION: {json.dumps(decision_record, indent=2)}")
        
        # Also save to structured log file
        decisions_file = Path("bmad_decisions.jsonl")
        with open(decisions_file, "a") as f:
            f.write(json.dumps(decision_record) + "\n")

    def log_bmad_installation(self, install_path: str, success: bool, details: str):
        """Log BMAD installation attempts"""
        install_record = {
            "timestamp": datetime.now().isoformat(),
            "event": "bmad_installation",
            "install_path": install_path,
            "success": success,
            "details": details
        }
        
        self.logger.info(f"BMAD_INSTALL: {json.dumps(install_record, indent=2)}")
        
        # Also save to structured log file
        decisions_file = Path("bmad_decisions.jsonl")
        with open(decisions_file, "a") as f:
            f.write(json.dumps(install_record) + "\n")

